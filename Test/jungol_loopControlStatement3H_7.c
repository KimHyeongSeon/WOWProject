#include "stdafx.h"
#include "stdlib.h"

int main()
{
	int num = 0;
	int i = 0;
	int j = 0;
	int k = 0;
	int inputNum = 0;
	char inputChar = 'A';

	scanf("%d", &num);

	for (i = 0; i < num; i++)
	{
		for (j = 0; j < num - i; j++)
		{
			printf("%c ", inputChar);
			inputChar++;
		}
		for (k = 0; k < i; k++)
		{
			printf("%d ", inputNum);
			inputNum++;
		}
		printf("\n");
	}



	system("pause");
	return 0;
}
